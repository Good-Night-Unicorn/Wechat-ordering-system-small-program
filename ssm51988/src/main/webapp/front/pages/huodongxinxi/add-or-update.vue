<script>
	import wPicker from "@/components/w-picker/w-picker.vue";

	export default {
		data() {
			return {
				ruleForm: {
												huodongmingcheng: '',
																fengmian: '',
																faburen: '',
																huodongjieshao: '',
																fabushijian: '',
																huodongxiangqing: '',
